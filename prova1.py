#Você está criando um programa para calcular a área de um retângulo. 
#O programa deve solicitar ao usuário que forneça a base e a
#altura do retângulo. Em seguida, o programa deve calcular a área usando a fórmula:
#Área=Base×Altura
#O programa deve exibir a área calculada.

altura = int(input("Digite a sua altura do seu retangulo: "))
base = int(input("Digite a sua base do seu retangulo: "))

area = base * altura
print(f"O valor da area é {area}")